export const SKILL_TOPIC_GUIDE = `# 创作方法论：「东西方思想对决」

本场对话遵循「东西方思想对决」公式 —— **一个永恒命题 × 两个教科书级人物 = 一场没有输赢的思想交锋**。卖点不是谁赢，而是「原来还能这么想」的认知颤动 + 结尾的格局升华。

## 选命题（人生母题）
从人物的价值体系里提炼一个大众一听就共鸣的人生根本命题，作为 conflict 的内核：
自由与命运 / 生与死 / 入世与出世 / 个人与集体 / 理性与情感 / 坚守与变通 / 英雄与罪人 / 爱与孤独 / 知与行 / 确定与怀疑 / 向外求 vs 向内合……
越是人人都纠结过的越好。conflict 字段写成「A 主张 X vs B 主张 Y」，是双方都不肯让步的真冲突。

## 优点互攻法（没有输赢的底层逻辑）
动笔前先回答：**A 最大的优点，在 B 眼里是什么缺点？反之呢？** 让每一方拿对方的最高价值去攻击对方——每一方的强项同时就是弱项，谁也压不死谁，平局才站得住。这条决定 stages 里立场对峙关键词的拟定方向。

## 四阶段交锋（stages 必须是这 4 个，层层递进、越逼越深）
stages 数组**严格输出 4 个**，依次对应下面四个子问句，每阶段从攻击对方上一阶段的软肋开始（递进 ≠ 平移）：
1. **方法论之争**——子问句「你怎么做？」
2. **价值观之争**——子问句「你为何这么做？」
3. **现实困境之争**——子问句「信念撞上现实、要付出代价时，还坚持吗？」
4. **终极命运/救赎之争**——子问句「面对结局、死亡或永恒，如何安放自己？」
- 每个 stage 的 title = 该阶段一个**画面感 / 机锋感的子问句小标题**（6-12 字，如「手段定不定罪？」「为谁而做才算英雄？」），不要写成干巴巴的"方法论之争"。
- 每个 stage 的 description = 一行立场对峙，格式 "「东方立场」 VS 「西方立场」"，立场关键词 3-7 字，应能从角色 SKILL.md 的表达 DNA / Mental Models / 经典概念里认出。

## 贯穿道具（写进 opener 与 setting）
给整场对决配一个**贯穿全场的具体道具 / 意象**（如老子vs佛陀的青牛与函谷关），把抽象玄谈拉回地面、也利于出图。把这个道具自然写进 opener（开场动作）和 setting（场景），让它开篇就出现、可被镜头拍到。

## 命题咬合
四阶段全程咬住命题本身，不比"谁更牛"。setting / opener 要画面感拉满（超现实相遇时空 + 具体光影 + 可拍道具）。`;

